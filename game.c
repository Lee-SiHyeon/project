int level_max_missiles[3] = {5, 8, 10};

void _Delay(int ms)
{
	volatile int i;
	for(i=0;i<(0x1000*ms);i++);
}

void Draw_Image(GameObject *obj) {
    Lcd_Set_Windows(obj->x, obj->y, obj->x + obj->width - 1, obj->y + obj->height - 1);

    int i=0, j=0;
 
    for (;i<obj->height;i++) {
        j = 0;
        for (;j<obj->width;j++){
            Lcd_Put_Pixel(obj->x+j, obj->y+i, obj->image[obj->width*i+j]);
        }
    }
}
 
void Clear_Image(GameObject *obj) {
    Lcd_Draw_Box(obj->x, obj->y, obj->width, obj->height, BLACK);
}

void Game_Init(void)
{
	Lcd_Clr_Screen();

    plane.x = 20;
    plane.y = Lcd_H/2 - PLANE_H/2;
    plane.prev_x = 0;
    plane.prev_y = 0;
	plane.width = PLANE_W;
	plane.height = PLANE_H;
	plane.image = Plane;
    plane.is_used = 1;

	score = 0;
	game_level = 0;
	game_state_flag = GAME_PLAYING;
    plane_move_flag = 1;
}

void Game_Missile_Generation(void)
{
    int i;
    for(i = 0; i < level_max_missiles[game_level]; i++)
    {
        if(missile[i].is_used == 0)
        {
            missile[i].x = 320;
            missile[i].y = rand() % (Lcd_H - 10);
            missile[i].width = MISSILE_W;
            missile[i].height = MISSILE_H;
            missile[i].image = Missile;
            missile[i].prev_x = 0;
            missile[i].prev_y = 0;
            missile[i].is_used = 1;
        }
    }
}

void Game_Bullet_Generation(void)
{
    volatile int i;
    for(i = 0; i < 10; i++)
    {
        if(bullet[i].is_used == 0)
        {
            bullet[i].x = plane.x + plane.width;
            bullet[i].y = plane.y + plane.height / 2;
            bullet[i].width = BULLET_W;
            bullet[i].height = BULLET_H;
            bullet[i].image = Bullet;
            bullet[i].is_used = 1;
            break;
        }
    }
}

void Game_Plane_Move(int dir)
{
    int dx = 10;
	int dy = 10;

    plane_move_flag = 1;

    plane.prev_x = plane.x;
    plane.prev_y = plane.y;

	switch(dir){ //1-left, 2-right, 3-down, 4-up
		case 1:
			if(plane.y >= 15) plane.y -= dy;
			break;
		case 2:
			if(plane.y <= 200) plane.y += dy;
			break;
		case 3:
			if(plane.x >= 15) plane.x -= dx;
			break;
		case 4:
			if(plane.x <= 300) plane.x += dx;
			break;
	}
}

void Game_Missile_Move(void)
{
    int i;
    int dx = -level_px_move[game_level];
    for(i = 0; i < level_max_missiles[game_level]; i++)
    {
        if (missile[i].is_used == 0)
            continue;
        missile[i].prev_x = missile[i].x;
        missile[i].prev_y = missile[i].y;

        missile[i].x += dx;

        if(missile[i].x <= 0)
        {
            missile[i].x = 0;
            missile[i].y = 0;
            missile[i].prev_x = 0;
            missile[i].prev_y = 0;
            missile[i].is_used = 0;
        }
    }
}

void Game_Bullet_Move(void)
{
    int i;
    int dx = level_px_move[game_level];
    for(i = 0; i < MAX_BULLET; i++)
    {
        if(bullet[i].is_used == 0)
            continue;

        bullet[i].prev_x = bullet[i].x;
        bullet[i].prev_y = bullet[i].y;
        
        bullet[i].x += dx;

        if(bullet[i].x >= Lcd_W - bullet[i].width)
        {
            bullet[i].x = 0;
            bullet[i].y = 0;
            bullet[i].prev_x = 0;
            bullet[i].prev_y = 0;
            bullet[i].is_used = 0;
        }
    }
}

int Check_Collision(GameObject* object1, GameObject* object2) {
    int left1 = object1->x;
    int right1 = object1->x + object1->width;
    int top1 = object1->y;
    int bottom1 = object1->y + object1->height;
    
    int left2 = object2->x;
    int right2 = object2->x + object2->width;
    int top2 = object2->y;
    int bottom2 = object2->y + object2->height;
    
    if (right1 >= left2 && left1 <= right2 && bottom1 >= top2 && top1 <= bottom2) {
        return 1;
    }
    return 0;
}

void Draw_LCD(void)
{
    int index, count, i, j;

	if(game_state_flag == GAME_START){
		Lcd_Draw_Back_Color(BLUE);
		_Delay(300);
		Lcd_Clr_Screen();
		_Delay(300);
	}

	else if(game_state_flag==GAME_PLAYING){
        Game_Missile_Move();

        // 미사일과 비행기 충돌 여부
        for(i = 0; i < level_max_missiles[game_level]; i++)
        {
            if (missile[i].is_used)
            {
                if(Check_Collision(&plane, &missile[i]))
                {
                    game_state_flag = GAME_OVER;
                	Uart_Printf("plane and missile Collision \n");
                    return;
                }
                    
            }
        }

        Game_Bullet_Move();
        // 미사일과 총알 충돌 여부
        for(i = 0; i < level_max_missiles[game_level]; i++)
        {
            if(missile[i].is_used == 0)
                continue;
            for(j = 0; j < MAX_BULLET; j++)
            {
                if(bullet[j].is_used == 0)
                    continue;
                if (Check_Collision(&missile[i], &bullet[j]))
                {
                    missile[i].x = 0;
                    missile[i].y = 0;
                    missile[i].prev_x = 0;
                    missile[i].prev_y = 0;
                    missile[i].is_used = 0;
                    
                    bullet[i].x = 0;
                    bullet[i].y = 0;
                    bullet[i].prev_x = 0;
                    bullet[i].prev_y = 0;
                    bullet[i].is_used = 0;
                }
            }
        }

        // 비행기 draw
        if(plane_move_flag) {
            Clear_Image(&plane);
            Draw_Image(&plane);
			plane_move_flag = 0;
		}

        // missile draw
        for(i = 0; i < level_max_missiles[game_level]; i++)
        {
            if(missile[i].is_used == 1)
            {
                Clear_Image(&missile[i]);
                Draw_Image(&missile[i]);
            }
        }

       // bullet draw
        for(i = 0; i < 10; i++)
        {
            if(bullet[i].is_used == 1)
            {
                Clear_Image(&bullet[i]);
                Draw_Image(&bullet[i]);
            }
        }
	}

	else if(game_state_flag==GAME_OVER){
		Lcd_Draw_Back_Color(RED);
		_Delay(300);
		Lcd_Clr_Screen();
		_Delay(300);
	}
}